package com.company.project.dto;

public class AerospikeProperties {
	private String host;
	private int port;
	private String name;
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int hostPort) {
		this.port = hostPort;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}


}
